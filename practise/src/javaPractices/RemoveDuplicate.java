package javaPractices;

import java.util.HashSet;
import java.util.Set;

public class RemoveDuplicate {
	public static void main(String args[])
	{
		 int rows = 5;
	        for(int i = 1; i <= rows; ++i) {
	            for(int j = 1; j <= i; ++j) {
	                System.out.print("*");
	            }
	            System.out.println();
	        }
	}


}
